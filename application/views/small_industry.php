Things to consider while Making Cake Gel<br><br>
While making cake gel, you need to consider the following;<br><br>
1.	Choice of emulsifier<br>
2.	Raw material properties<br>
3.	Recipe (ingredient ratio)<br>
4.	Blending technology<br>
