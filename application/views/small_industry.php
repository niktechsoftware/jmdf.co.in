What is Cake Gel Made Of?<br><br>
Cake gels are semi-liquid made up of water, emulsifiers, solvents, and sugar. Raw materials like vegetable oil, G.M.S., Xanthene’s gum, Satiric Acid etc are basic ingredients. It is stable (at room temperature) gel-like product.<br><br>
